package com.alumni.struts.form;

public class fileForm {

	private int id;
	private int pid;
	private String fname;
	public void setId(int id) {
		this.id = id;
	}
	public int getId() {
		return id;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public int getPid() {
		return pid;
	}
	public void setFname(String fname) {
		this.fname = fname;
	}
	public String getFname() {
		return fname;
	}
	
	
}
